        "    elif not uploaded_pdf_file:\n",
        "        st.error(\"Please upload your PDF translation file.\")\n",
        "    else:\n",
        "        with st.spinner(\"Parsing PDF dictionary and scanning image text... Please wait...\"):\n",
        "\n",
        "            # --- Stream Pointer Resets ---\n",
        "            uploaded_pdf_file.seek(0)\n",
        "            uploaded_image_file.seek(0)\n",
        "\n",
        "            translation_map = {}\n",
        "            excel_choices = []\n",
        "            valid_pdf = True\n",
        "\n",
        "            # --- 1. SAFE PDF PARSING ---\n",
        "            try:\n",
        "                pdf_reader = PyPDF2.PdfReader(io.BytesIO(uploaded_pdf_file.read()))\n",
        "                if len(pdf_reader.pages) == 0:\n",
        "                    st.error(\"❌ The uploaded PDF appears to be empty.\")\n",
        "                    valid_pdf = False\n",
        "            except Exception as e:\n",
        "                st.error(\"❌ Invalid PDF File. Please ensure you uploaded a true PDF document.\")\n",
        "                st.info(f\"Technical details: {str(e)}\")\n",
        "                valid_pdf = False\n",
        "\n",
        "            if valid_pdf:\n",
        "                try:\n",
        "                    for page in pdf_reader.pages:\n",
        "                        page_text = page.extract_text()\n",
        "                        if not page_text:\n",
        "                            continue\n",
        "\n",
        "                        for line in page_text.split('\\n'):\n",
        "                            line = line.strip()\n",
        "                            if not line:\n",
        "                                continue\n",
        "\n",
        "                            parts = []\n",
        "                            if ',' in line:\n",
        "                                parts = line.split(',', 1)\n",
        "                            elif ':' in line:\n",
        "                                parts = line.split(':', 1)\n",
        "                            else:\n",
        "                                # Find transitions between non-ASCII (Chinese) and ASCII character spaces\n",
        "                                for idx, char in enumerate(line):\n",
        "                                    if idx > 0 and ord(char) < 128 and ord(line[idx-1]) >= 128:\n",
        "                                        parts = [line[:idx].strip(), line[idx:].strip()]\n",
        "                                        break\n",
        "\n",
        "                            if len(parts) == 2:\n",
        "                                source_text = parts[0].strip()\n",
        "                                translation_text = parts[1].strip()\n",
        "\n",
        "                                if source_text and translation_text:\n",
        "                                    translation_map[source_text.lower()] = translation_text\n",
        "                                    excel_choices.append(source_text)\n",
        "\n",
        "                    if not translation_map:\n",
        "                        st.error(\"❌ Layout Parsing Error: No valid translation pairs found inside this file.\")\n",
        "                        st.info(\"💡 Ensure your PDF uses clear indicators like: '张飞, Zhang Fei' or '张飞: Zhang Fei'.\")\n",
        "                        valid_pdf = False\n",
        "\n",
        "                except Exception as e:\n",
        "                    st.error(f\"❌ Failed to extract dictionary content: {str(e)}\")\n",
        "                    valid_pdf = False\n",
        "\n",
        "\n",
        "            # --- 2. SAFE IMAGE PROCESSING & BOX OVERLAY ---\n",
        "            if valid_pdf:\n",
        "                try:\n",
        "                    original_image = Image.open(io.BytesIO(uploaded_image_file.read())).convert(\"RGBA\")\n",
        "                    base_image = original_image.copy()\n",
        "\n",
        "                    # Convert to NumPy array for EasyOCR ingestion\n",
        "                    img_np = np.array(base_image)\n",
        "                    ocr_results = reader.readtext(img_np, detail=1)\n",
        "\n",
        "                    if not ocr_results:\n",
        "                        st.warning(\"❓ No text blocks detected in this image layout.\")\n",
        "                    else:\n",
        "                        draw_layer = ImageDraw.Draw(base_image)\n",
        "                        matches_count = 0\n",
        "\n",
        "                        for box, text_raw, confidence in ocr_results:\n",
        "                            text_clean = text_raw.strip()\n",
        "                            if not text_clean or not excel_choices:\n",
        "                                continue\n",
        "\n",
        "                            # Fuzzy match extraction\n",
        "                            best_match, score = process.extractOne(\n",
        "                                text_clean,\n",
        "                                excel_choices,\n",
        "                                scorer=fuzz.token_sort_ratio\n",
        "                            )\n",
        "\n",
        "                            if score >= user_threshold:\n",
        "                                translation_text = str(translation_map[best_match.lower()])\n",
        "                                matches_count += 1\n",
        "\n",
        "                                # Extract structural dimensions\n",
        "                                x_coords = [p[0] for p in box]\n",
        "                                y_coords = [p[1] for p in box]\n",
        "                                x_min, y_min = int(min(x_coords)), int(min(y_coords))\n",
        "                                x_max, y_max = int(max(x_coords)), int(max(y_coords))\n",
        "\n",
        "                                box_width = x_max - x_min\n",
        "                                box_height = y_max - y_min\n",
        "\n",
        "                                # Solid opaque mask to wipe clean original source characters\n",
        "                                draw_layer.rectangle([x_min, y_min, x_max, y_max], fill=(15, 15, 15, 255))\n",
        "\n",
        "                                # Dynamic Font Calculation Fallback\n",
        "                                font_size = max(10, int(box_height * 0.75))\n",
        "                                try:\n",
        "                                    font = ImageFont.truetype(\"arial.ttf\", font_size)\n",
        "                                except IOError:\n",
        "                                    font = ImageFont.load_default()\n",
        "\n",
        "                                # Draw the translation centered vertically inside the mask block\n",
        "                                draw_layer.text((x_min + 4, y_min + (box_height - font_size) // 4),\n",
        "                                                translation_text, fill=(255, 255, 255, 255), font=font)\n",
        "\n",
        "                        st.success(f\"✨ Successfully replaced {matches_count} text blocks!\")\n",
        "\n",
        "                        # --- CONVERT PROCESSED IMAGE TO DOWNLOADABLE BUFFER ---\n",
        "                        # Convert to RGB to ensure clean, standardized high-res PNG serialization\n",
        "                        final_output_image = base_image.convert(\"RGB\")\n",
        "                        img_buffer = io.BytesIO()\n",
        "                        final_output_image.save(img_buffer, format=\"PNG\", quality=100)\n",
        "                        img_buffer.seek(0)\n",
        "\n",
        "                        # --- EXPORT ACTION INTERFACE ---\n",
        "                        st.markdown(\"### 3. Save Translated File\")\n",
        "                        st.download_button(\n",
        "                            label=\"💾 Download Translated High-Res PNG\",\n",
        "                            data=img_buffer,\n",
        "                            file_name=\"translated_overlay_output.png\",\n",
        "                            mime=\"image/png\",\n",
        "                            type=\"secondary\"\n",
        "                        )\n",
        "\n",
        "                        # --- DISPLAY SIDE-BY-SIDE RESULT COLS ---\n",
        "                        out_col1, out_col2 = st.columns(2)\n",
        "                        with out_col1:\n",
        "                            st.subheader(\"Original Image\")\n",
        "                            st.image(original_image, use_container_width=True)\n",
        "                        with out_col2:\n",
        "                            st.subheader(\"Translated Overlay\")\n",
        "                            st.image(base_image, use_container_width=True)\n",
        "\n",
        "                except Exception as img_err:\n",
        "                    st.error(\"❌ Invalid Image File. Please verify your uploaded file.\")\n",
        "                    st.info(f\"Technical details: {str(img_err)}\")\n",
