        "import io\n",
        "import streamlit as st\n",
        "import easyocr\n",
        "import PyPDF2\n",
        "from PIL import Image, ImageDraw, ImageFont\n",
        "from thefuzz import process, fuzz\n",
        "import numpy as np\n",
        "\n",
        "# 1. APPLICATION SETUP & PAGE LAYOUT\n",
        "st.set_page_config(layout=\"wide\", page_title=\"Image Translation Overlay Tool\")\n",
        "st.title(\"🌐 Image Translation Overlay Tool\")\n",
        "\n",
        "# 2. CACHE OCR READER (Prevents reloading on every user interaction)\n",
        "@st.cache_resource\n",
        "def load_ocr_reader():\n",
        "    # Pre-loads models for Simplified Chinese and English\n",
        "    return easyocr.Reader(['ch_sim', 'en'])\n",
        "\n",
        "try:\n",
        "    reader = load_ocr_reader()\n",
        "except Exception as ocr_init_err:\n",
        "    st.error(\"Failed to initialize EasyOCR engine. Ensure system dependencies are met.\")\n",
        "    st.stop()\n",
        "\n",
        "# 3. INTERFACE BUILDER\n",
        "st.markdown(\"### 1. Upload Files & Adjust Sensitivity\")\n",
        "\n",
        "col1, col2 = st.columns(2)\n",
        "with col1:\n",
        "    uploaded_image_file = st.file_uploader(\"Upload Image File\", type=[\"png\", \"jpg\", \"jpeg\"])\n",
        "with col2:\n",
        "    uploaded_pdf_file = st.file_uploader(\"Upload PDF Translation Dictionary\", type=[\"pdf\"])\n",
        "\n",
        "user_threshold = st.slider('Match Sensitivity %:', min_value=30, max_value=100, value=60, step=5)\n",
        "\n",
        "st.markdown(\"### 2. Execute Overlay\")\n",
        "execute_btn = st.button(\"Translate and Overlay Text\", type=\"primary\")\n",
        "\n",
