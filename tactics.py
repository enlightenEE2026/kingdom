import io
import os
import sys
import subprocess
import importlib

# 1. DEPENDENCY MANAGEMENT
def ensure_libraries():
    required_packages = {
        "easyocr": "easyocr",
        "PyPDF2": "PyPDF2",
        "thefuzz": "thefuzz[speed]",
        "PIL": "pillow",
        "matplotlib": "matplotlib",
        "numpy": "numpy",
        "streamlit": "streamlit",
    }
    for module_name, pip_name in required_packages.items():
        try:
            importlib.import_module(module_name)
        except ImportError:
            try:
                subprocess.check_call([sys.executable, "-m", "pip", "install", pip_name])
            except subprocess.CalledProcessError:
                sys.exit(1)

ensure_libraries()

import streamlit as st
import easyocr
import PyPDF2
from PIL import Image, ImageDraw, ImageFont
from thefuzz import process, fuzz
import numpy as np

# 2. CACHE OCR READER
@st.cache_resource
def load_ocr_reader():
    return easyocr.Reader(['ch_sim', 'en'])

reader = load_ocr_reader()

# 3. STREAMLIT APP LAYOUT
st.set_page_config(layout="wide")
st.title("🌐 Image Translation Overlay Tool")

st.markdown("### 1. Upload Files & Adjust Sensitivity")

col1, col2 = st.columns(2)
with col1:
    uploaded_image_file = st.file_uploader("Upload Image File", type=["png", "jpg", "jpeg"])
with col2:
    uploaded_pdf_file = st.file_uploader("Upload PDF Translation Dictionary", type=["pdf"])

# Global Configuration Sliders (Must exist outside the execution button block)
config_col1, config_col2 = st.columns(2)
with config_col1:
    user_threshold = st.slider('Match Sensitivity %:', min_value=30, max_value=100, value=60, step=5)
with config_col2:
    zoom_factor = st.slider(
        "Render Resolution Quality:",
        min_value=1.0,
        max_value=3.0,
        value=1.0,
        step=0.5,
        help="Higher values upscale the layout canvas for crystal clear text rendering."
    )

st.markdown("### 2. Execute Overlay")
execute_btn = st.button("Translate and Overlay Text", type="primary")

if execute_btn:
    if not uploaded_image_file:
        st.error("Please upload an image file.")
    elif not uploaded_pdf_file:
        st.error("Please upload your PDF translation file.")
    else:
        with st.spinner("Parsing PDF dictionary and scanning image text... Please wait..."):

            # --- 1. SAFE PDF VALIDATION & PARSING ---
            translation_map = {}
            excel_choices = []
            valid_pdf = True

            try:
                pdf_data = uploaded_pdf_file.read()
                pdf_file = io.BytesIO(pdf_data)
                pdf_reader = PyPDF2.PdfReader(pdf_file)

                if len(pdf_reader.pages) == 0:
                    st.error("❌ The uploaded PDF appears to be empty.")
                    valid_pdf = False

            except Exception as e:
                st.error("❌ Invalid PDF File. Please ensure you uploaded a true PDF document.")
                st.info(f"Technical details: {str(e)}")
                valid_pdf = False

            if valid_pdf:
                try:
                    for page in pdf_reader.pages:
                        page_text = page.extract_text()
                        if not page_text:
                            continue

                        lines = page_text.split('\n')
                        for line in lines:
                            line = line.strip()
                            if not line:
                                continue

                            parts = []
                            if ',' in line:
                                parts = line.split(',', 1)
                            elif ':' in line:
                                parts = line.split(':', 1)
                            else:
                                for idx, char in enumerate(line):
                                    if idx > 0 and ord(char) < 128 and ord(line[idx-1]) >= 128:
                                        parts = [line[:idx].strip(), line[idx:].strip()]
                                        break

                            if len(parts) == 2:
                                source_text = parts[0].strip()
                                translation_text = parts[1].strip()

                                if source_text and translation_text:
                                    translation_map[source_text.lower()] = translation_text
                                    excel_choices.append(source_text)

                    if not translation_map:
                        st.error("❌ Layout Parsing Error: No valid translation pairs found inside this file.")
                        st.info("💡 Ensure your PDF uses clear indicators like: '张飞, Zhang Fei' or '张飞: Zhang Fei'.")
                        valid_pdf = False

                except Exception as e:
                    st.error(f"❌ Failed to extract dictionary content: {str(e)}")
                    valid_pdf = False

            # --- 2. SAFE IMAGE PROCESSING ---
            if valid_pdf:
                try:
                    image_data = uploaded_image_file.read()
                    original_image = Image.open(io.BytesIO(image_data)).convert("RGBA")

                    # Upscale base canvas dimensions matching user zoom preference
                    if zoom_factor > 1.0:
                        new_w = int(original_image.width * zoom_factor)
                        new_h = int(original_image.height * zoom_factor)
                        work_image = original_image.resize((new_w, new_h), Image.Resampling.LANCZOS)
                    else:
                        work_image = original_image.copy()

                    ocr_results = reader.readtext(np.array(original_image), detail=1)

                    if not ocr_results:
                        st.warning("❓ No text blocks detected in this image layout.")
                    else:
                        draw_layer = ImageDraw.Draw(work_image)

                        base_font_size = 14
                        scaled_font_size = int(base_font_size * zoom_factor)

                        try:
                            font = ImageFont.truetype("DejaVuSans.ttf", scaled_font_size)
                        except IOError:
                            try:
                                font = ImageFont.truetype("arial.ttf", scaled_font_size)
                            except IOError:
                                font = ImageFont.load_default()

                        matches_count = 0

                        for box, text_raw, confidence in ocr_results:
                            text_clean = text_raw.strip()
                            if not text_clean:
                                continue

                            if not excel_choices:
                                continue

                            best_match, score = process.extractOne(text_clean, excel_choices, scorer=fuzz.token_sort_ratio)

                            if score >= user_threshold:
                                translation_text = str(translation_map[best_match.lower()])
                                matches_count += 1

                                # Extract coordinates and scale them up to match canvas zoom multiplier
                                x_coords = [p[0] * zoom_factor for p in box]
                                y_coords = [p[1] * zoom_factor for p in box]
                                x_min, y_min = int(min(x_coords)), int(min(y_coords))
                                x_max, y_max = int(max(x_coords)), int(max(y_coords))

                                # Draw background mask rectangle
                                draw_layer.rectangle([x_min, y_min, x_max, y_max], fill=(15, 15, 15, 245))

                                # Centered modern bounding box math
                                text_bbox = draw_layer.text_bbox((0, 0), translation_text, font=font)
                                text_w = text_bbox[2] - text_bbox[0]
                                text_h = text_bbox[3] - text_bbox[1]

                                box_w = x_max - x_min
                                box_h = y_max - y_min

                                text_x = x_min + (box_w - text_w) // 2
                                text_y = y_min + (box_h - text_h) // 2

                                draw_layer.text((text_x, text_y), translation_text, fill=(255, 255, 255, 255), font=font)

                        st.success(f"✨ Successfully replaced {matches_count} text blocks!")

                        # --- CONVERT OVERLAY IMAGE FOR DOWNLOAD ---
                        output_buffer = io.BytesIO()
                        work_image.convert("RGB").save(output_buffer, format="PNG", quality=100)
                        processed_image_bytes = output_buffer.getvalue()

                        # CSS container constraints to manage large zoomed canvases inside browser frames
                        st.markdown(
                            """
                            <style>
                            div[data-testid="stColumn"] {
                                overflow: auto !important; max-height: 75vh;
                            }
                            </style>
                            """,
                            unsafe_allow_html=True
                        )

                        # --- DISPLAY IMAGES SIDE BY SIDE ---
                        out_col1, out_col2 = st.columns(2)
                        with out_col1:
                            st.subheader("Original Image")
                            st.image(original_image, use_container_width=True)
                        with out_col2:
                            st.subheader("Translated Overlay")
                            st.image(work_image, use_container_width=True)
                            st.download_button(label="💾 Download Razor-Sharp Image",data=processed_image_bytes,file_name="sharp_translated_overlay.png",mime="image/png",type="secondary")

                except Exception as img_err:
                    st.error("❌ Invalid Image File. Please verify your uploaded file is a healthy image graphic.")
                    st.info(f"Technical details: {str(img_err)}")